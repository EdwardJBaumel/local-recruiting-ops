"""
HELPER CONFIG + SPRITE MANIFEST

The helper is the cosmetic companion on the dashboard. This module owns:

  a. The authoritative default - fresh installs get Joby.
  b. A sprite MANIFEST that points at animated GIFs served out of
     sentinel-ui/public/sprites/. Each sprite has multiple animation
     states (idle / celebrate / sleep) so the dashboard can switch
     the visible GIF based on user activity and milestones. Art is
     generated by scripts/build_sprites.py - regenerate whenever the
     palette or shape changes.
  c. The sayings catalogue - enough lines that the speech bubble
     doesn't repeat itself on a typical session.
  d. `validate_patch`, `resolve`, `read_helper`, `update_helper` so the
     UI gets a fully-populated renderable view every time.

All functions are pure. Persistence lives in user_store - helper is
stored as a nested dict under `user.helper`.
"""
