@dataclass(frozen=True)
class HelperView:
    """Serialisable view used by /api/helper. `assets` is the full
    state->url map so the frontend can swap animations locally without
    refetching. `asset_url` is the convenience default (idle state) for
    simple renderers."""
    name: str
    sprite: str
    eyes: str
    accessory: str
    asset_url: str
    assets: dict[str, str]
    label: str
    credit: str

    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "sprite": self.sprite,
            "eyes": self.eyes,
            "accessory": self.accessory,
            "asset_url": self.asset_url,
            "assets": dict(self.assets),
            "label": self.label,
            "credit": self.credit,
        }


def default_helper() -> dict:
    """Fresh independent copy of the default helper."""
    return dict(DEFAULT_HELPER)


def _clean_name(raw: Any) -> Optional[str]:
    if not isinstance(raw, str):
        return None
    name = raw.strip()
    if not name:
        return None
    return name[:MAX_NAME_LENGTH]


def validate_patch(patch: Any) -> dict:
    """Return a dict safe to merge into stored helper state. Unknown
    keys dropped. Out-of-range values dropped (not clamped) so a bad
    patch is a no-op rather than a silent reset."""
    if not isinstance(patch, dict):
        return {}
    cleaned: dict = {}
    if "name" in patch:
        name = _clean_name(patch["name"])
        if name is not None:
            cleaned["name"] = name
    if "sprite" in patch and patch["sprite"] in SPRITES:
        cleaned["sprite"] = patch["sprite"]
    if "eyes" in patch and patch["eyes"] in EYE_STYLES:
        cleaned["eyes"] = patch["eyes"]
    if "accessory" in patch and patch["accessory"] in ACCESSORIES:
        cleaned["accessory"] = patch["accessory"]
    return cleaned


def resolve(stored: Any) -> HelperView:
    """Merge stored helper dict with defaults, attach sprite metadata.
    Always returns a renderable view."""
    merged = default_helper()
    if isinstance(stored, dict):
        merged.update(validate_patch(stored))
    sprite_data = SPRITES.get(merged["sprite"]) or SPRITES[DEFAULT_SPRITE]
    assets = dict(sprite_data["assets"])
    return HelperView(
        name=merged["name"],
        sprite=merged["sprite"],
        eyes=merged["eyes"],
        accessory=merged["accessory"],
        asset_url=assets[DEFAULT_STATE],
        assets=assets,
        label=sprite_data["label"],
        credit=sprite_data["credit"],
    )


def read_helper(data_dir) -> HelperView:
    from core import user_store
    try:
        user = user_store.load(data_dir)
    except Exception:
        user = {}
    return resolve((user or {}).get("helper"))


def update_helper(data_dir, patch: Any) -> HelperView:
    from core import user_store
    cleaned = validate_patch(patch)
    current = user_store.load(data_dir)
    existing = current.get("helper") if isinstance(current.get("helper"), dict) else {}
    merged = dict(existing or default_helper())
    merged.update(cleaned)
    user_store.update(data_dir, {"helper": merged})
    return resolve(merged)


def list_options() -> dict:
    """Everything the settings UI needs. Sprite entries include the
    full state->asset map so the picker can preview idle animations
    and hint at celebrate/sleep variants."""
    return {
        "sprites": [
            {
                "key": key,
                "label": data["label"],
                "description": data["description"],
                "assets": dict(data["assets"]),
                "asset_url": data["assets"][DEFAULT_STATE],
                "credit": data["credit"],
            }
            for key, data in SPRITES.items()
        ],
        "eyes": list(EYE_STYLES),
        "accessories": list(ACCESSORIES),
        "states": list(STATES),
        "happy_tiers": [dict(t) for t in HAPPY_TIERS],
        "burst_ms": dict(BURST_MS),
        "name_limits": {"min": MIN_NAME_LENGTH, "max": MAX_NAME_LENGTH},
        "default": dict(DEFAULT_HELPER),
    }


def sayings(mood: str = "idle") -> list[str]:
    """Return the saying list for a mood, or the idle list if unknown."""
    return list(SAYINGS.get(mood) or SAYINGS["idle"])
